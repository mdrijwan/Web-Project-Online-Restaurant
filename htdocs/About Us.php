<p>The winds of Malaysia called them ashore and it was there, under the warm sun, in the rich soil that they discovered the Mexican Spices on mixture of few highly confidential ingredients. A spice like no other, they used it to create a unique sauce that put fire in their bellies and ignited passion in their souls.</p>
<p>A few  years later (in 2016 to be exact), it was the same taste of spicy sauces that inspired Ahson , Avinesh, Rezwan to invite their lovely lecturer Dr Ashley to a humble dinner in the heart of Kota Damansara, Petaling jaya to try some mouth-watering Spicy sauces marinated and grilled mexican chicken.</p>
